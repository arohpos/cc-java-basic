package main.chap2;

public class Main {

	public static void main(String[] args) {
		//課題
		double radius = 10;
		final double pi = 3.14;
		double area = radius * radius * pi;
		System.out.println("半径" + radius + "の円の面積は、" + area + "です。");
		
		//プログラミング演習
		int count;
		int price;
		int total;
		count = 5;
		price = 1000;
		total = count * price;
		System.out.print("お支払いは、" + total + "円です。");

	}

}
